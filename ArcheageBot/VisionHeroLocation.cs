        public static string GetCurrentDirection(int HeroCircleForm, int x, int y)
        {
            //0 - 100%
            //1 - 70%
            //2 - Не идет проверка
            //3 - 50%
            //4 - 50%
            //5 - 80% 
            //6 - 
            if ( (HeroCircleForm == 0) || (Hero.ForceUpdateDirection == true) )
            {
                Hero.NoUpdateDirectionIteration = 0;
                Dictionary<string, int> SummColors = new Dictionary<string, int>();
                SummColors.Add("North", 0);
                SummColors.Add("West", 0);
                SummColors.Add("East", 0);
                SummColors.Add("South", 0);
                SummColors.Add("North–West", 0);
                SummColors.Add("North–East", 0);
                SummColors.Add("South–West", 0);
                SummColors.Add("South–East", 0);

                for (int i = 0; i < VisionHeroLocation.HeroCircles[HeroCircleForm].Count; ++i)
                {

                    if (VisionHeroLocation.HeroCircles[HeroCircleForm][i].Object == true)
                    {
                        Color CurrentColor = ScreenCapture.LockBitmap.Screen.GetPixel(x + VisionHeroLocation.HeroCircles[HeroCircleForm][i].OffsetX, y + VisionHeroLocation.HeroCircles[HeroCircleForm][i].OffsetY);
                        int CurrentColorSumm = CurrentColor.R + CurrentColor.G + CurrentColor.B;

                        for (int DescriptionCount = 0; DescriptionCount < VisionHeroLocation.HeroCircles[HeroCircleForm][i].Description.Count; ++DescriptionCount)
                        {
                            SummColors[VisionHeroLocation.HeroCircles[HeroCircleForm][i].Description[DescriptionCount]] = SummColors[VisionHeroLocation.HeroCircles[HeroCircleForm][i].Description[DescriptionCount]] + CurrentColorSumm;
                        }
                    }

                }

                int LighterSummColor = 0;
                string ReturnDirection = "Undefined";

                foreach (KeyValuePair<string, int> SummColor in SummColors)
                {
                    if (SummColor.Value > LighterSummColor)
                    {
                        LighterSummColor = SummColor.Value;
                        ReturnDirection = SummColor.Key;
                    }
                }

                return ReturnDirection;
            }
            else
            {
                Hero.NoUpdateDirectionIteration++;
                return Hero.Direction;
            }
        }


